package designpattern.chapter6.Prototype2.DeepShallow;

import java.util.Set;

public class Age {
	
	int year;
	int value;
	
	
	
	public Age(int year, int value) {
		super();
		this.year = year;
		this.value = value;
	}
	
	
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}

	
	
}
